int MediumSystem::dimension() const
{
    int result = 1;
    for (auto medium : _media) result = max(result, medium->dimension());
    return result;
}

////////////////////////////////////////////////////////////////////

int MediumSystem::numMedia() const
{
    return _media.size();
}

////////////////////////////////////////////////////////////////////

int MediumSystem::numCells() const
{
    return _volumev.size();
}

